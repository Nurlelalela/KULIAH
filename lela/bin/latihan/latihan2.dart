void main() {
  // Menyimpan panjang dan lebar dalam variabel bertipe double
  double panjang = 12.5;
  double lebar = 7.3;

  // Menghitung luas dan keliling
  double luas = panjang * lebar;
  double keliling = 2 * (panjang + lebar);

  // Menampilkan hasil dengan format rapi
  print("=== Perhitungan Persegi Panjang ===");
  print("Panjang : $panjang");
  print("Lebar   : $lebar");
  print("Luas    : $luas");
  print("Keliling: $keliling");
}
